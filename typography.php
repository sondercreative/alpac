				<h4>Under-footprint Sector Effects</h4>
			  <p class="lead">To understand what specific development activities are most impacting old forest bird species in the Alpac FMA are, the under-footprint figures shows how species relative abundance is predicted to change within each footprint compared to the habitat it replaced. (Figure: Birds Within Sector Effects.) </p>
			  <p>With very few exceptions, all types of human disturbances greatly decrease habitat suitability for bird species associated with old deciduous/mixedwood forests in the Alpac FMA because these activities impact their preferred habitat.</p>
			  <p>Within forestry footprint, the populations of all but 2 old forest bird species are predicted to be less abundant than expected compared to the habitat it replaces. </p>
			</div>
			<div class="column is-one-half-desktop">
			  <h4>Sector Effects on Regional Bird Populations</h4>
			  <p class="lead">The region population figure shows the predicted change in the total relative abundance of old forest bird species across the Alpac FMA area due to each sector’s footprint. (Figure: Bird Regional Sector Effects.)</p>
			  <p>In general, regional effects are much less than under-footprint effects because a great deal of old forest bird habitat has not been disturbed by anthropogenic activities in the Alpac FMA.</p>
			  <p>For energy, transportation, and urban/industrial footprint, regional population effects of industrial sectors on birds associated with old deciduous forest scale was small—between -2.5% and +1%.</p>
			  <p>Forestry footprint resulted in largest predicted loss of suitable habitat for most old forest birds—on average -7%—because harvesting disturbs the most native vegetation, and because forestry targets the species’ preferred old-forest habitat.</p>
