                <ul>
                    <li><span class="green">Human-created Waterbodies:</span> Man-made waterbodies created for a variety of purposes, such as to extract fill (burrow-pits, sumps), water livestock (dugouts), transport water (canals), meet municipal needs (water supply and sewage), and store water (reservoirs).</li>
                    <li><span class="green">Transportation Footprint:</span> Railways, roadways and trails with hard surfaces such as cement, asphalt, or gravel, roads or trails without gravel or pavement, and the vegetation strips alongside transportation features.</li>
                    <li><span class="green">Urban/Industrial Footprint:</span> Combines urban, rural, and industrial footprint types including residences, buildings, and disturbed vegetation and substrate associated with urban and rural settlements, such as housing, shopping centres, industrial areas, golf courses, and recreation areas, as well as bare ground cleared for industrial and commercial development.</li>
                </ul>
