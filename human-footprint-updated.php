                <h5>How Human Footprint is Analyzed and Reported</h5>
                <p>The ABMI monitors the status of Alberta’s human footprint using satellite imagery at two spatial scales:</p>
                <h5 class="intro">3 × 7 -km Samples</h5> 
                <p>The ABMI uses human footprint data measured annually at a 1:5,000 scale to track changes in human footprint over time. Detailed annual samples of human footprint are measured in a 3 × 7-km rectangular area centred near each of the ABMI’s 1,656 sites, which when summed across all sites amounts to about 5% of the province’s land surface. ABMI human footprint trend data are available from 1999 to 2017, except for 2000-2003 (inclusive). Trend data and the metadata associated with these data can be accessed here.</p>
                <h5 class="intro">Human Footprint Recovery</h5> 
                <p>As a successional footprint, forestry recovers with time after disturbance. To account for this recovery, we summarize “recovered forestry”, prorating older harvested areas using biotic recovery curves based on a literature review.</p>
