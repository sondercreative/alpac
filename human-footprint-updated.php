                <p>Human footprint is divided intro six categories for analysis:</p>
                <ul>
                    <li><span class="green">Agriculture Footprint:</span> Areas of annual or perennial cultivation, including crops and tame pasture, as well as confined feeding operations and other high-density livestock areas.</li>
                    <li><span class="green">Energy Footprint: </span> Areas where vegetation or soil has been disturbed by the creation of mine sites, peat mines, pipelines, seismic lines, transmission lines, well sites, and wind-generation facilities. This footprint type is called “energy footprint” because most of this footprint type is associated with the energy industry.</li>
                    <li><span class="green">Forestry Footprint:</span> Areas in forested landscapes where timber resource extraction has occurred for industrial purposes, including clear-cut and partial-cut logging methods.</li>
                </ul>
            </div>

